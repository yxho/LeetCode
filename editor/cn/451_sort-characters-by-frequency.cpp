class Solution {
public:
    string frequencySort(string s) {
        unordered_map<char, int> m;
        for (auto i:s) {
            m[i]++;
        }
        auto cmp = [&](auto &a, auto &b) {
            return a.second < b.second;
        };
        priority_queue<pair<char, int>, vector<pair<char, int>>,  decltype(cmp)> q(cmp);
        for(auto item:m){
            q.push(item);
        }
        string res;

        while(!q.empty()){
            auto cur = q.top();
            q.pop();
            res+=string(cur.second,cur.first);
        }
        return res;
    }
};
