class Solution {
public:
  double myPow(double x, int n) {
    if (n == 0)
      return 1;
    if (n == 1)
      return x;
    if (n == -1)
      return 1 / x;
    double res1 = myPow(x, n / 2);
    double res2 = myPow(x, n % 2);
    return res1 * res1 * res2;
  }

};
